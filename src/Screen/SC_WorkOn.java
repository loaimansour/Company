    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        String pNo = (String)Tools.InputBox("Enter Project Number");
        String eNo = (String)Tools.InputBox("Enter Employee Number");
        w.setPROJECTNO(Integer.parseInt(eNo));
        w.setEMPNO(Integer.parseInt(eNo));
        String s = "select * from WORKON_DATA "
                +" where EMPLOYEE_NO= "+eNo+" and "+ "PROJECT_NO="+ pNo;
        conn.FillTable(s, tblw);
        
    }//GEN-LAST:event_btnFindActionPerformed
  private void setV(){
      String E = (String)Ecom.getSelectedItem();
      String P = (String)Pcom.getSelectedItem();
      
      String em = e.getValueByName(E);
      String Pr = p.getValueByName(P);
      w.setEMPNO(Integer.parseInt(em));
      w.setPROJECTNO(Integer.parseInt(Pr));
      
  }
