    private void jButtonBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBox1ActionPerformed
       String st= "select * from WORKON_DATA "
               +" WHERE ";
        if(rdoENo.isSelected()){
            st+="EMPLOYEE_NO  ";
        }
        else if(rdoEName.isSelected()){
            st+="EMPLOYEE_NAME ";
        }
        else if(rdoPNo.isSelected()){
            st+="PROJECT_NO  ";
        }
        else {
            st+="PROJECT_NAME  ";
        }
        st+=" like'%"+txtSearch.getText()+"%'"; 
        w.getCustomRows(st, tblw);
        
    }//GEN-LAST:event_jButtonBox1ActionPerformed

