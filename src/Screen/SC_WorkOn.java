    private void tblwMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblwMouseClicked
        int row = tblw.getSelectedRow();
        String En = (String)tblw.getValueAt(row, 1);
        String Pn = (String)tblw.getValueAt(row, 3);
        
        Ecom.setSelectedItem(En);
        Pcom.setSelectedItem(Pn);
    }//GEN-LAST:event_tblwMouseClicked

