    public void set(){
        p.setProjectNo(Integer.parseInt(txtNumber.getText()));
        p.setProjectName(txtName.getText());
        p.setLocation(txtLocation.getText());
       
        
        String com = compDept.getSelectedItem().toString();
        String Valcom = d.getValueByName(com);
        p.setDeptNo(Integer.parseInt(Valcom));
    }
