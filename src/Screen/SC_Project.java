    private void tblProjectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProjectMouseClicked
       try{
            int row  = tblProject.getSelectedRow();
            
       String Pno = tblProject.getValueAt(row, 0).toString();
       String Pname = tblProject.getValueAt(row, 1).toString();
       String Loc = tblProject.getValueAt(row, 2).toString();
       
       String DeptN = tblProject.getValueAt(row, 3).toString();
       
       txtNumber.setText(Pno);
       txtName.setText(Pname);
       txtLocation.setText(Loc);
       compDept.setSelectedItem(DeptN);
      btnAdd.setEnabled(false);
      btnUpdate.setEnabled(true);
      btnDelete.setEnabled(true);
       }
       catch(Exception ex ){
           Tools.msgBox(ex.getMessage());
       }
    }//GEN-LAST:event_tblProjectMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
       String s  = "select * from PROJECT_DATA "
               + " WHERE ";
       if(rdoNumber.isSelected()){
           s+=" PROJECT_NO ";
       }
       else if (rdoName.isSelected()){
            s+=" PROJECT ";
       }
       else if(rdoLocation.isSelected()){
           s+=" LOCATION ";
       }
       else{
           s+=" DEPARTMENT_NO ";
       }
       String str = s+=" like'%"+txtSearch.getText()+"%'";
       p.getCustomRows(str, tblProject);
    }//GEN-LAST:event_btnSearchActionPerformed
     
